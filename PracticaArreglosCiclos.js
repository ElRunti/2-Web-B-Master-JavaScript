let frutas = ["manzana","pera","papaya","platano"];


for(let conteoFrutas = 0; conteoFrutas < frutas.length; conteoFrutas++){
    console.log(`Fruta numero ${conteoFrutas + 1}. ${frutas[conteoFrutas]} `)
}
let conteoFrutasWhile = 0;
while(conteoFrutasWhile < frutas.length){
    console.log(`Fruta numero ${conteoFrutasWhile + 1}. ${frutas[conteoFrutasWhile]} `);

    conteoFrutasWhile++;
}